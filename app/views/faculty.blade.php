                <h3 class="heading-agileinfo" style="text-align: left;font-size: 25px !important;margin-bottom: 10px;">SCIU Faculty Regulation:</h3>

                All faculty members teach at a degree level appropriate to their qualifications, and have graduated from accredited universities from around the world.
                Faculty members develop their own syllabus for each course, select the textbooks and other materials to be used, and this is reviewed by at least one administrator who is a senior faculty member, to ensure overall quality. All faculty members also participate in the process of academic planning.<br><br>

                The mission, purpose, and goals of the Institution are a major part of each faculty member's teaching, as this relates to how they teach, how they grade, and how they interact with students. In the annual academic meetings, each faculty member participates in accomplishing the SCIU mission, purpose and goals.<br><br>
                Students are awarded a degree upon completion of all courses in that degree program. The Registrar at each campus coordinates this with the SCIU administration to ensure both organizations are recognized on the award.<br><br>
                Certified copies of transcripts as well as full curriculum vitae are obtained prior to subcontracting any faculty members, and the administrator in charge also contacts the degree-granting institution to verify the person's qualifications and degrees. This is always done with authorization from the candidate.<br><br>
                The number of hours needed for direct interaction between students and faculty per term vary according to the type of programs and mode of delivery, but it is expected that a minimum of three hours interaction is provided regardless of online or face-to-face mode.<br><br>
                The number of hours faculty are required to spend on student evaluation vary widely depending on the type and length of assignment, as well as the ability/speed of the faculty member to read each assignment. It is expected that at least 15 minutes per weekly assignment per student will be spent, with a maximum of 45 minutes per student weekly assignment.
